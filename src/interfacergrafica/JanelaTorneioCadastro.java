    private Date[] data(){
        try {
            DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            int inicioDia = cmbInicioDia.getSelectedIndex();
            int inicioMes = cmbInicioMes.getSelectedIndex();
            int inicioAno = cmbInicioAno.getSelectedIndex();

            int terminoDia = cmbTerminoDia.getSelectedIndex();
            int terminoMes = cmbTerminoMes.getSelectedIndex();
            int terminoAno = cmbTerminoAno.getSelectedIndex();
            
            

            String dataInicio = cmbInicioDia.getItemAt(inicioDia) + "/"
                    + cmbInicioMes.getItemAt(inicioMes) + "/"
                    + cmbInicioAno.getItemAt(inicioAno);

            Date daI = df.parse(dataInicio);

            String dataTermino = cmbTerminoDia.getItemAt(terminoDia) + "/" 
                    + cmbTerminoMes.getItemAt(terminoMes) + "/"
                    + cmbTerminoAno.getItemAt(terminoAno);

            Date daT = df.parse(dataTermino);
            
            return new Date[]{daI, daT};
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
            return null;
        }
    }
    
    @Override
    public void popularCombo(){
        try {
            cmbCategoria.removeAllItems();
            ArrayList<String> listaCat = CategoriaAbstrata.getListaDeFabricas();
            
            listaCat.forEach(categoria -> {
                cmbCategoria.addItem(categoria);
            });
            
            for (int i = 1; i <= 31; i++) {
                cmbInicioDia.addItem(i+"");
                cmbTerminoDia.addItem(i+"");
            }
            
            for (int i = 1; i <= 12; i++) {
                cmbInicioMes.addItem(i+"");
                cmbTerminoMes.addItem(i+"");
            }
            
            for (int i = 2018; i <= 2023; i++) {
            cmbInicioAno.addItem(Integer.toString(i));
            cmbTerminoAno.addItem(Integer.toString(i));
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
    private void preencherTela(Torneio torneio){
        try {
            DateTime dataIn = new DateTime(torneio.getDataInicio());
            DateTime dataTer = new DateTime(torneio.getDataTermino());
            
            System.out.println(dataIn.getDayOfMonth());
            System.out.println(dataIn.getMonthOfYear());
            System.out.println(dataIn.getYear());
            
            txtCodigo.setText(torneio.getId() + "");
            txtDescricao.setText(torneio.getDescricao());
            cmbCategoria.setSelectedItem(torneio.getCategoria().getDescricao());
            cmbCategoria.setEnabled(false);
            
            cmbInicioDia.setSelectedItem(dataIn.getDayOfMonth()+"");
            cmbInicioMes.setSelectedItem(dataIn.getMonthOfYear()+"");
            cmbInicioAno.setSelectedItem(dataIn.getYear()+"");
            
            cmbTerminoDia.setSelectedItem(dataTer.getDayOfMonth()+"");
            cmbTerminoMes.setSelectedItem(dataTer.getMonthOfYear()+"");
            cmbTerminoAno.setSelectedItem(dataTer.getYear()+"");
            
            txtTaxaInscricao.setText(torneio.getTaxa()+"");
            btnExcluir.setEnabled(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
