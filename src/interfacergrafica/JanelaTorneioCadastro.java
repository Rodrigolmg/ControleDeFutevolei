    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        try {
            if(!txtCodigo.getText().isEmpty()){
                int resposta = JOptionPane.showConfirmDialog(null, "Confirma a "
                        + "exclusão do campeonato?", "Sistema de Controle de Campeonato", 
                        JOptionPane.YES_NO_OPTION);
                if(resposta == JOptionPane.YES_OPTION){
                    new NTorneio().excluir(Integer.parseInt(txtCodigo.getText()));
                    JOptionPane.showMessageDialog(null, "Operação efetuada com sucesso!");

                    limparTela();
                }
            } else{
                JOptionPane.showMessageDialog(null, "Insira o código do campeonato"
                        + " a ser excluído!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

