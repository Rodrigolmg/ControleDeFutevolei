    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {
            int inicioDia = cmbInicioDia.getSelectedIndex();
            int inicioMes = cmbInicioMes.getSelectedIndex();
            
            int terminoDia = cmbTerminoDia.getSelectedIndex();
            int terminoMes = cmbTerminoMes.getSelectedIndex();
            
            if(inicioMes == 1 || terminoMes == 1){
                if(inicioDia > 28 || terminoDia > 28)
                    throw new Exception("Data inválida!");
            }
            
            ArrayList<String> listS = new ArrayList<>();
            listS.add(txtDescricao.getText());
            listS.add(txtTaxaInscricao.getText());
            Validation.isEmpty(listS);
            
            Torneio torneio = new Torneio(cmbCategoria.getSelectedIndex()+1);
            torneio.setDescricao(txtDescricao.getText());
            torneio.setDataInicio(new java.sql.Date(data()[0].getTime()));
            torneio.setDataTermino(new java.sql.Date(data()[1].getTime()));
            torneio.setTaxa(Double.parseDouble(txtTaxaInscricao.getText().replace(',', '.')));
            
            //se for diferente de nulo e diferente de vazio...
            if(txtCodigo.getText() != null && !txtCodigo.getText().isEmpty()){
                torneio.setId(Integer.parseInt(txtCodigo.getText()));
            }
            new NTorneio().incluir(torneio);
            JOptionPane.showMessageDialog(null, "Operação executada com sucesso");
            limparTela();
                JOptionPane.showMessageDialog(null, "Campos vazios!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

